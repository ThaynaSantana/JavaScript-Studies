// Operadores de comparação

// Comparando os valores e retornar um Boolean 
// como resposta á comparação.

let one = 1
let two = 2

//  ==    igual a
console.log(two == 1)
console.log(one == "1")

//  !=    diferente de
console.log(one != two)
console.log(one != 1)
console.log(one != '1')

//  ===   ESTRITAMENTE IGUAL A
// compara o valor e o tipo tbm se é iguais
console.log(one === '1')
console.log(one === 1)

// !==    ESTRITAMENTE DIFERENTE DE
// compara o valor e o tipo tbm se são diferentes
console.log(two !== '2')
console.log(two !== 2)

//  >		Maior que
console.log(one > two)

// >=		Maior igual que
console.log(one >= two)

// <		Menor que
console.log(one < two)

// <=		Menor igual que
console.log(one <= two)
console.log(two <= one)