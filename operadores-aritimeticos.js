// 	Operadores Aritiméticos

// multiplicação *
console.log(3 * 6)

// divisão /
console.log(10 / 5)

// soma +
console.log(9 + 9)

// subtração -
console.log(7-3)

// resto da divisão(modulo) %
console.log(12 % 2)

// incremento ++
let a = 2
console.log(a++) // or console.log(++a)

// decremento --
let b = 20
console.log(b--) // or console.log(--b)

// exponencial **
console.log(30 ** 2)