// Operadores de atribuicao (Assignment)

// assignment
x = 1

// addition assignment
// x = x + 2
x += 2

// subtraction assignment
// x = x -1
x -= 1

// multiplication assignment
// x = x * 5
x *= 5

// division assignment
// x = x / 3
x /= 3 

// remainder (modulo)
x %= 2

// exponetiation
x **= 2
